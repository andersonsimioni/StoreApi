using System.Text.RegularExpressions;

namespace StoreApi.Shared.Services.ExceptionSecurityChecker;

public class ExceptionSecurityChecker : IExceptionSecurityChecker
{
    private readonly List<Type> TrustedExceptions;

    public void BlockExceptionType(Type exceptionType)
    {
        if(this.TrustedExceptions.Contains(exceptionType))
            this.TrustedExceptions.Remove(exceptionType);
    }

    public void TrustExceptionType(Type exceptionType)
    {
        if(!this.TrustedExceptions.Contains(exceptionType))
            this.TrustedExceptions.Add(exceptionType);
    }
    
    public bool IsSecure(Exception _exception)
    {
        if(!this.TrustedExceptions.Any(t => _exception.GetType() == t)) return false;

        var msg = _exception.Message;
        var regList = new []{
            //IPV4 DETECT
            new Regex(@"(\b25[0-5]|\b2[0-4][0-9]|\b[01]?[0-9][0-9]?)(\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)){3}"),
            //IPV6 DETECT
            new Regex(@"(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))"),
            //PORT DETECT
            new Regex(@"^((6553[0-5])|(655[0-2][0-9])|(65[0-4][0-9]{2})|(6[0-4][0-9]{3})|([1-5][0-9]{4})|([0-5]{0,5})|([0-9]{1,4}))$"),
            //CREDIT CARD
            new Regex(@"(^4[0-9]{12}(?:[0-9]{3})?$)|(^(?:5[1-5][0-9]{2}|222[1-9]|22[3-9][0-9]|2[3-6][0-9]{2}|27[01][0-9]|2720)[0-9]{12}$)|(3[47][0-9]{13})|(^3(?:0[0-5]|[68][0-9])[0-9]{11}$)|(^6(?:011|5[0-9]{2})[0-9]{12}$)|(^(?:2131|1800|35\d{3})\d{11}$)")
        };    
        return !regList.Any(r => r.IsMatch(msg));
    }

    public ExceptionSecurityChecker(params Type[] trustedExceptions){
        this.TrustedExceptions = new List<Type>();
        this.TrustedExceptions.AddRange(trustedExceptions);
    }
}